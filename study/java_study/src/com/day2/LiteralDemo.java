package com.day2;

public class LiteralDemo {
    public static void main(String[] args) {
        //1 整数
        System.out.println(66);
        //2 小数
        System.out.println(3.14);
        //3 字符 必须单引号，仅有一个字符
        System.out.println('h');
        //4 字符串 必须双引号围起来，内容随意
        System.out.println("hellow world");
        //5 布尔类型 java为小写
        System.out.println(true);
        //6 空类型 null
    }
}
