package com.day4;

public class DoWhileDemo {
    public static void main(String[] args) {
        //do-while循环
        int i = 0 ;
        do{
            System.out.println("helow world!");
            i++;
        }while(i<3);

        for(int j=0;j<3;j++){
            System.out.println("hello world");
        }
    }
}
